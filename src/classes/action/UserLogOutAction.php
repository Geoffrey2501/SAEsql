<?php

namespace iutnc\NRV\action;

use iutnc\NRV\action\Action;

class UserLogOutAction extends Action
{
    public function execute(): string
    {
        if (isset($_SESSION['user'])) {
            unset($_SESSION['user']);
        }
        session_destroy();
        return 'Vous êtes déconnecté';
    }
}




